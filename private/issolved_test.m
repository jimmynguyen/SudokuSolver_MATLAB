function issolved_test

	fprintf('\nTesting issolved.m ...\n\n');

	% Test 1
	board = [1 2 3, 4 5 6, 7 8 9;...
			 4 5 6, 7 8 9, 1 2 3;...
			 7 8 9, 1 2 3, 4 5 6;...

			 2 3 4, 5 6 7, 8 9 1;...
			 5 6 7, 8 9 1, 2 3 4;...
			 8 9 1, 2 3 4, 5 6 7;...

			 3 4 5, 6 7 8, 9 1 2;...
			 6 7 8, 9 1 2, 3 4 5;...
			 9 1 2, 3 4 5, 6 7 8];
	fprintf('Test 1: %s\n', assertTrue(issolved(board)));

	% Test 2
	board(end) = 10;
	fprintf('Test 2: %s\n', assertFalse(issolved(board)));

	fprintf('\n');

end